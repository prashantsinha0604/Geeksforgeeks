class Solution
{
    public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        // code goes here.
        Node * i = head1 , * j = head2 ;
        Node * dummyNode = new Node(3);
        Node* temp = dummyNode;
        
        while( i and j )
        {
            if( i-> data == j -> data )
            {
                Node* tempNode = new Node( i -> data );
                temp -> next = tempNode;
                i = i -> next;
                j = j -> next;
                temp = temp -> next;
            }
            else if( i -> data < j -> data )
            {
                i = i -> next ;
            }
            else
            {
                j = j -> next ;
            }
        }
        
        return dummyNode -> next;
    }
};



